-- Trigger for notifying admins on new complaint
CREATE OR REPLACE FUNCTION public.notify_admins_on_complaint()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.notifications (user_id, title, message)
  SELECT id, 'New Complaint Filed', 'A new complaint "' || NEW.title || '" has been filed.'
  FROM public.profiles
  WHERE role = 'admin';
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_complaint_created
  AFTER INSERT ON public.complaints
  FOR EACH ROW EXECUTE FUNCTION public.notify_admins_on_complaint();

