ALTER TABLE public.feedback ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Feedback is viewable by everyone." ON public.feedback
  FOR SELECT USING (true);

CREATE POLICY "Users can insert feedback for their resolved complaints." ON public.feedback
  FOR INSERT WITH CHECK (
    auth.uid() = user_id AND
    EXISTS (
      SELECT 1 FROM public.complaints 
      WHERE id = complaint_id AND user_id = auth.uid() AND status = 'Resolved'
    )
  );

