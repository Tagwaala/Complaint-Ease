CREATE POLICY "Comments are viewable by relevant parties." ON public.comments
  FOR SELECT USING (
    EXISTS (
      SELECT 1 FROM public.complaints 
      WHERE id = complaint_id 
      AND (
        user_id = auth.uid() OR 
        (SELECT role FROM public.profiles WHERE id = auth.uid()) IN ('admin', 'team')
      )
    )
  );

CREATE POLICY "Relevant parties can insert comments." ON public.comments
  FOR INSERT WITH CHECK (
    auth.uid() = user_id AND
    EXISTS (
      SELECT 1 FROM public.complaints 
      WHERE id = complaint_id 
      AND (
        user_id = auth.uid() OR 
        (SELECT role FROM public.profiles WHERE id = auth.uid()) IN ('admin', 'team')
      )
    )
  );

-- 11. Create Feedback/Rating table
CREATE TABLE public.feedback (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  complaint_id BIGINT REFERENCES public.complaints(id) ON DELETE CASCADE NOT NULL UNIQUE,
  user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
